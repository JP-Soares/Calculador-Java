    public void display(){
        if(contDisplay <= 5){//the user can type 5 numbers
            if(contClick >= 2){
                for(int a = 1; a <= contClick; a++){
                    if(a == 2){
                        txtDisplay.setText(""+previousTypedNum[2]+typedNum);
                        System.out.println("CASE 2");
                    }else if(a == 3){    
                        txtDisplay.setText(""+previousTypedNum[2]+previousTypedNum[3]+typedNum);
                        System.out.println("CASE 3");
                    }else if(a == 4){
                        txtDisplay.setText(""+previousTypedNum[2]+previousTypedNum[3]+previousTypedNum[4]+typedNum);
                        System.out.println("CASE 4");
                    }else if(a == 5){
                        txtDisplay.setText(""+previousTypedNum[2]+previousTypedNum[3]+previousTypedNum[4]+previousTypedNum[5]+typedNum);
                        System.out.println("CASE 5");
                    }
                }
            }else{
                txtDisplay.setText(""+typedNum);
            }
        }
        
        contDisplay++;
        
    }
    
    public void reset(){
        cal.setResult(0);
        txtDisplay.setText("");
        contClick = 0;
        contDisplay = 0;
        typedNum = 0;
        
        for(int i = 0; i < previousTypedNum.length; i++){
            previousTypedNum[i] = 0;
        }
    }


    private void btnResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResultActionPerformed
        cal.setCurrentNum(Double.parseDouble(txtDisplay.getText()));
        
        if(cal.getOperation().equals("sum")){
            cal.sum();
        }else if(cal.getOperation().equals("minus")){
            cal.minus();
        }else if(cal.getOperation().equals("multiple")){
            cal.multiple();
        }else if(cal.getOperation().equals("division")){
            cal.division();
        }
        txtDisplay.setText(""+cal.getResult());
    }//GEN-LAST:event_btnResultActionPerformed

